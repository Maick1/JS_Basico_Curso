//operadores de comparacion

let a = 5;
console.log(a > 5); //mayor q
console.log(a < 5); //mayor q
console.log(a >= 5); //mayor o igual
console.log(a <= 5); //menor o igual

///casi no se usa
console.log(a == 5); //igualdad
console.log(a != 5); //Distinto o diferente

//usados actualmente
console.log(a === '10'); //diferente
console.log(a !== '10'); //diferente


